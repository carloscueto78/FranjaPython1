# Slices / rebanadas

nombre = "francisco" 

print( nombre[0] )
print( nombre[3] )

#Posiciones Fra
print( nombre[0:3] )

# Fra pero sin poner el 0
print( nombre[:3] )

# desde la posicion 3 hasta el final (ncisco)
print( nombre[3:])

# desde la posicion 1 hasta la 7 (rnacis)
print( nombre[1:7])

#desde la posicion 1 hasta la 7 pero de 2 en 2 (agregando otro ' : ') (rni)
print( nombre[1:7:2] )

#desde el principio hasta el final y de 1 en 1 (por defecto)
print( nombre[::] )

#desde la posicion 1 hasta el final y de 3 en 3 (rcc)
print( nombre[1::3])

#parte desde el principio hasta el final pero en orden inverso (ocsicnarf)
print( nombre[::-1] )




